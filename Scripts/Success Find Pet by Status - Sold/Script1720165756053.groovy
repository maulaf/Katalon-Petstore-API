//valid status : available, pending, sold
def status = "sold"

ResponseObject response = WS.sendRequest(findTestObject('Object Repository/findPetsByStatus', [('status') : status]))

WS.verifyResponseStatusCode(response, 200)

WS.comment("Got response:\n" + response.getResponseBodyContent())

def jsonResponse = new JsonSlurper().parseText(response.getResponseText())

// Iterate through each item in the JSON response and verify the status
for (def item : jsonResponse) {
	assert item.status == status : "Expected status to be '${status}', but got '${item.status}'"
}